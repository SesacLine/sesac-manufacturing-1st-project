"""게이트 정규식 패턴 모음 (데이터만 — 판정 로직은 각 gate에 둔다).

config에 의존하지 않으므로 OPENAI_API_KEY 없이 import 가능하다.
"""
from __future__ import annotations

import re

# 명령형 종결(문장끝/구두점)일 때만 매칭 — 뒤로 말이 이어지면(예: "재가동해 본 적",
# "돌려 봐도 되나요?", "재가동해야 하나요?") 질문/서술이므로 제외해 오버블록을 막는다.
# bare 해/돌려에만 쓰며, 반드시 동작 동사에 인접시켜 뒤쪽 다른 동사 오매칭을 막는다.
_TERM = r"(?=$|[.!?,])"

# intake_gate: 입력 단계에서 위험 실행 '요청'을 잡는 deterministic backstop
# 질문형("해도 되나요?", "해야 하나요?")은 _TERM·접미사 조건 때문에 자연히 빠진다(LLM이 ANSWER_SAFELY로 분류).
FORBIDDEN_PATTERNS = [
    # 점검/검사 없이·생략·빼고 + 재가동/기동/운전 + 명시적 명령 어미.
    r"(점검|검사)\s*(없이|전에?|안\s*하고|생략하고|생략하여|빼고|빼먹고|건너뛰고)\s*(재?가동|기동|운전)"
    r".{0,20}(해줘|해주세요|하라|하세요|시켜줘|시켜|강행|합시다|계속\s*해|그냥\s*(해|진행))",
    # 점검/검사 없이 + 재가동/기동/운전 + bare 해(동작 동사 바로 뒤, 명령형 종결).
    # bare 해는 .{0,20} 다리 없이 동사에 인접할 때만 잡는다 — 뒤쪽의 '위험해'/'궁금해'/'설명해'
    # 같은 다른 동사에 latch되어 안전 경고·질문을 오차단하던 회귀를 막기 위함.
    r"(점검|검사)\s*(없이|전에?|안\s*하고|생략하고|생략하여|빼고|빼먹고|건너뛰고)\s*(재?가동|기동|운전)해" + _TERM,
    # 점검/검사 없이·빼고 + 돌려/돌리(그 자체가 명령형 종결).
    r"(점검|검사)\s*(없이|안\s*하고|빼고|빼먹고|생략하고|건너뛰고)\s*돌(려|리)" + _TERM,
    # 안전장치/안전커버/인터락 우회·해제·떼 + 운전/가동/돌려.
    # (운전해/가동해의 bare 해는 동작 동사 어간 운전·가동이 이미 잡으므로 별도 bare 해 불필요.)
    r"(안전\s*장치|안전\s*커버|인터?락|인터록|interlock)\S*\s*(우회|해제|끄|꺼|무시|떼|뗀|분리|제거)"
    r".*(돌려|돌리|가동|운전|진행|기동)",
    # 경고/알람/위험(alarm) 무시 + 가동/운전.
    r"(경고|알람|위험|alarm)\s*\S*\s*무시.*(가동|운전|계속|진행)",
    # 재가동/기동/가동 강행.
    r"(재가동|기동|가동)\s*\S*\s*(강행|밀어붙|그냥\s*(해|진행))",
]

# intake_gate: control command 관측 플래그(is_control_command)용
CONTROL_COMMAND_PATTERN = r"가동|재가동|기동|운전|정지|승인|우회|해제|LOTO"

# output_safety_gate: 최종 답변에서 위험 실행 '지시'를 잡는 deterministic backstop
# (매치 주변에 SAFE_NEGATION이 있으면 _contains_unsafe_execution_instruction이 안전 권고로 보고 통과)
OUTPUT_FORBIDDEN_PATTERNS = [
    # 점검/검사 없이·생략·빼고 (+ 그냥/바로 등 부사) + 재가동/기동/운전 + 지시 어미.
    r"(점검|검사)\s*(없이|전에?|안\s*하고|생략하고|생략하여|빼고|빼먹고|건너뛰고)"
    r"(?:\s*(?:그냥|바로|곧장|당장|즉시|일단))?\s*(재?가동|기동|운전)"
    r".{0,20}(해도\s*(됩니다|된다|돼)|하세요|하라|가능|승인|계속)",
    # 안전장치/안전커버/인터락 우회·해제·풀 + 지시 어미.
    r"(안전\s*장치|안전\s*커버|인터?락|인터록|interlock)\S*\s*(우회|해제|풀|끄|꺼|무시|떼|뗀|분리|제거)"
    r".{0,30}(하세요|하라|해도|됩니다|가능|운전|계속|진행)",
    # 경고/알람/위험(alarm) 무시 + 가동/운전.
    r"(경고|알람|위험|alarm)\s*\S*\s*무시.{0,30}(가동|운전|계속|진행|하세요|하라)",
]

# output_safety_gate: 매치 주변 부정/경고어 → 안전 권고로 보고 통과(오차단 방지)
# 단 '위험'은 OUTPUT_FORBIDDEN_PATTERNS의 '위험 무시' 트리거와 겹치므로, '위험 무시' 콜로케이션은
# 부정어로 보지 않는다(그렇지 않으면 "위험 무시하고 운전하세요"가 안전 권고로 오인되어 통과됨).
SAFE_NEGATION = re.compile(r"피하|하지\s*마|마라|마세요|말아|금지|않|불가|위험(?!\s*무시)|안\s*됩니다|안\s*돼|삼가|자제|주의")
